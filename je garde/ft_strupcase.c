#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 96 && str[i] < 123)
			str[i] = str[i] - 32;
		i++;
	}
	return str;
}

int	main(void)
{
	char	str[] = "J'ai faim";
	printf("%s\n", ft_strupcase(str));
	return (0);
}
