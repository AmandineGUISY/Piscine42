int	ft_is_prime(int nb)
{
	int	n;

	n = nb - 1;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (0);
	while (nb % n != 0 && n != 1)
	{
		n--;
	}
	if (n != 1)
		return (0);
	return (1);
}
