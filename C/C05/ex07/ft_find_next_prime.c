int	ft_find_next_prime(int nb)
{
	int	i;

	i = 3;
	if (nb <= 2)
		return (2);
	if (nb % 2 == 0)
		nb++;
	while (nb % i != 0 && i < nb / 2)
	{
		i = i + 2;
	}
	if (nb % i == 0)
		return (ft_find_next_prime(nb + 1));
	return (nb);
}
